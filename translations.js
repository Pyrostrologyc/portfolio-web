// Set initial language
let currentLang = localStorage.getItem('appLang') || 'es';

function setLanguage(lang) {
    if (!translations[lang]) return;
    currentLang = lang;
    localStorage.setItem('appLang', lang);
    applyTranslations();
    
    // Update active state in UI
    document.querySelectorAll('.lang-flag').forEach(flag => {
        if (flag.dataset.lang === lang) {
            flag.classList.add('active');
        } else {
            flag.classList.remove('active');
        }
    });

    // Re-render grids to apply dynamic translations
    if (typeof updateN3D === 'function') updateN3D();
    else if (typeof initDataAndSearch === 'function') initDataAndSearch();
}

function applyTranslations() {
    const dict = translations[currentLang];
    
    // Elements with data-i18n
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (dict[key]) {
            // Check if it's an input/textarea placeholder or normal text
            if (el.tagName === 'INPUT' && el.type !== 'submit' && el.type !== 'button') {
                el.placeholder = dict[key];
            } else if (el.tagName === 'TEXTAREA') {
                el.placeholder = dict[key];
            } else {
                el.textContent = dict[key];
            }
        }
    });

    // Elements with data-i18n-placeholder specifically (some inputs might need it)
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (dict[key]) {
            el.placeholder = dict[key];
        }
    });
}

// Initial setup on DOM ready
document.addEventListener('DOMContentLoaded', () => {
    // Add event listeners to flags
    document.querySelectorAll('.lang-flag').forEach(flag => {
        flag.addEventListener('click', () => {
             setLanguage(flag.dataset.lang);
        });
    });
    
    // Apply current language
    setLanguage(currentLang);
});
